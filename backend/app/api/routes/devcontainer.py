# ── Stack extraction helpers ──────────────────────────────────────────────────

def _languages_from_stack(detected_stack: dict) -> list[str]:
    """Map detected_stack backend/frontend framework names to language identifiers."""
    lang_map = {
        "FastAPI": "python", "Django": "python", "Flask": "python",
        "Next.js": "node", "React": "node", "Vue": "node", "Svelte": "node",
        "Express": "node", "TypeScript": "node",
        "Go": "go", "Gin": "go", "Echo": "go",
        "Spring": "java", "Rust": "rust",
    }
    langs: list[str] = []
    for category in ("backend", "frontend", "runtime"):
        for item in detected_stack.get(category, []):
            name = item.get("name", "") if isinstance(item, dict) else item
            lang = lang_map.get(name)
            if lang and lang not in langs:
                langs.append(lang)
    return langs or ["python"]


def _frameworks_from_stack(detected_stack: dict) -> list[str]:
    """Extract lower-cased framework names from detected_stack."""
    seen: list[str] = []
    for category in ("backend", "frontend"):
        for item in detected_stack.get(category, []):
            name = item.get("name", "") if isinstance(item, dict) else item
            key = name.lower().replace(".", "").replace("-", "")  # "Next.js" → "nextjs"
            if name and key not in seen:
                seen.append(key)
    return seen


# ── ZIP builder ───────────────────────────────────────────────────────────────

_SETUP_TEMPLATE = textwrap.dedent("""\
    #!/usr/bin/env bash
    # Auto-generated by Atlas Devcontainer Generator
    set -euo pipefail

    {body}

    echo "✓ Atlas devcontainer setup complete"
""")


def _build_setup_sh(config: dict) -> str:
    cmd = config.get("postCreateCommand", "")
    lines = [c.strip() for c in cmd.split("&&") if c.strip()] if cmd else []
    body = "\n".join(lines) if lines else 'echo "No post-create commands defined."'
    return _SETUP_TEMPLATE.format(body=body)


def _build_zip(config: dict, version: int) -> io.BytesIO:
    buf = io.BytesIO()
    with zipfile.ZipFile(buf, "w", zipfile.ZIP_DEFLATED) as zf:
        zf.writestr(".devcontainer/devcontainer.json", json.dumps(config, indent=2))
        zf.writestr(".devcontainer/setup.sh", _build_setup_sh(config))
        zf.writestr(
            "README.md",
            f"# Atlas Devcontainer (v{version})\n\n"
            "Open this folder in VS Code and click **Reopen in Container**, "
            "or push to GitHub and launch a Codespace from the repository page.\n",
        )
    buf.seek(0)
    return buf


# ── Config generator (LLM with deterministic fallback) ────────────────────────

_logger = __import__("logging").getLogger(__name__)


async def _build_config_dict(
    evidence: dict | None,
    languages: list[str],
    frameworks: list[str],
    services: list[str],
    custom_features: list[str],
) -> dict:
    """Try the LLM path; fall back to the deterministic generator on any failure.

    All LLM output is secret-masked before being returned.
    """
    if evidence:
        try:
            raw = await generate_devcontainer_config(evidence)
            # Mask any secrets Claude may have echoed from README / env examples
            for field in ("postCreateCommand", "containerEnv"):
                if isinstance(raw.get(field), str):
                    raw[field] = SecretDetector.mask_all_secrets(raw[field])
            raw.setdefault("remoteUser", "vscode")
            _logger.info("devcontainer_llm_generation_success")
            return raw
        except Exception as exc:
            _logger.warning("devcontainer_llm_generation_failed: %s — using deterministic fallback", exc)

    config = DevcontainerGenerator.generate(
        languages=languages,
        frameworks=frameworks,
        services=services,
        custom_features=custom_features,
    )
    return DevcontainerGenerator.to_json(config)


