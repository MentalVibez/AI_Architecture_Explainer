import type { Metadata } from "next";
import { getResult } from "@/lib/api";
import DeveloperSummary from "@/components/DeveloperSummary";
import HiringManagerSummary from "@/components/HiringManagerSummary";
import DiagramPanel from "@/components/DiagramPanel";

interface Props {
  params: { id: string };
}

export async function generateMetadata({ params }: Props): Promise<Metadata> {
  try {
    const result = await getResult(Number(params.id));
    const evidence = (result.raw_evidence?.[0] as Record<string, unknown>) ?? {};
    const repo = (evidence.repo as Record<string, string>) ?? {};
    const repoLabel =
      repo.owner && repo.name ? `${repo.owner}/${repo.name}` : `Report #${params.id}`;
    return {
      title: `${repoLabel} — Architecture Analysis | CodebaseAtlas`,
      description: `Architecture diagram, framework detection, and plain-English summaries for ${repoLabel}. Generated by CodebaseAtlas.`,
      openGraph: {
        title: `${repoLabel} — CodebaseAtlas Report`,
        description: `Architecture diagram, framework detection, and summaries for ${repoLabel}.`,
        url: `https://www.codebaseatlas.com/results/${params.id}`,
      },
      robots: { index: false },
    };
  } catch {
    return {
      title: "Analysis Report | CodebaseAtlas",
      robots: { index: false },
    };
  }
}

export default async function ResultPage({ params }: Props) {
  const result = await getResult(Number(params.id));

  const evidence = (result.raw_evidence?.[0] as Record<string, unknown>) ?? {};
  const repo = (evidence.repo as Record<string, string>) ?? {};
  const filesScanned = (evidence.tree_paths as string[])?.length ?? 0;
  const repoLabel =
    repo.owner && repo.name ? `${repo.owner}/${repo.name}` : null;

  return (
    <div className="max-w-5xl mx-auto px-6 py-14 space-y-10">

      {/* Header */}
      <header className="border-b border-[#1a1a1a] pb-8">
        <div className="flex items-center gap-3 mb-4">
          <span className="font-mono text-[10px] tracking-[0.3em] text-[#c8a96e] uppercase">
            Atlas Report
          </span>
          <span className="h-px w-8 bg-[#1e1e1e]" />
          <a href="/" className="font-mono text-[10px] tracking-widest text-[#2a2a2a] hover:text-[#4a4a4a] transition-colors uppercase">
            ← New analysis
          </a>
        </div>
        <h1 className="font-serif text-4xl sm:text-5xl text-[#e8e0d4] mb-3">
          {repoLabel ?? `Result #${result.id}`}
        </h1>
        <div className="flex flex-wrap gap-4 font-mono text-[11px] text-[#3a3a3a]">
          {filesScanned > 0 && <span>{filesScanned} files scanned</span>}
          <span>Report #{result.id}</span>
        </div>
      </header>

      {/* Architecture diagram */}
      {result.diagram_mermaid && (
        <DiagramPanel mermaid={result.diagram_mermaid} />
      )}

      {/* Stack Detection + Technical summary */}
      <DeveloperSummary result={result} />

      {/* Non-Technical summary */}
      <HiringManagerSummary result={result} />

    </div>
  );
}
